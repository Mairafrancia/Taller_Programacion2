package CLASES;

/**
 * Representa el rol o cargo que ocupa un miembro dentro del cuerpo técnico.
 * @author Florencia Benitez
 * @author Agustina Barreto
 * @author Francia Maira
 * @author Gabriela Yañez
 */
public enum Rol {

    /** Ayudante de campo o asistente técnico. */
    AYUDANTECAMPO,

    /** Preparador físico encargado del rendimiento de los jugadores. */
    PREPARADORFISICO,

    /** Médico principal del plantel. */
    MEDICO,

    /** Kinesiólogo encargado de la prevención y rehabilitación de lesiones. */
    KINESIOLOGO,

    /** Entrenador específico para los arqueros del equipo. */
    ENTRENADORARQUEROS,

    /** Analista de videos encargado de estudiar al propio equipo y a los rivales. */
    ANALISTAVIDEOS,

    /** Nutricionista encargado de la alimentación del plantel. */
    NUTRICIONISTA,

    /** Psicólogo deportivo orientado al bienestar mental de los futbolistas. */
    PSICOLOGO;
}